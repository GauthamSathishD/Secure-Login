    /**
     * Writes the users to the users file
     */
    private void write() {
        //WRITE IN NEW USER
        try {
            try (PrintWriter outUsers = new PrintWriter(users)) {//writing object for first names                

                //print the arrays
                for (int c = 0; c < firstNames.size(); c++) {//keep writing to the user file as long as there are users

                    outUsers.print(firstNames.get(c));
                    outUsers.print(";");

                    outUsers.print(lastNames.get(c));
                    outUsers.print(";");

                    outUsers.print(usernames.get(c));
                    outUsers.print(";");

                    outUsers.print(emailAddresses.get(c));
                    outUsers.print(";");

                    outUsers.print(passwords.get(c));
                    outUsers.println(";");
                                     
                }
                
                outUsers.close();
            }
        } catch (IOException ex) {
            System.out.printf("ERROR: %s\n", ex);
        }
    }

    /**
     * Reads the user information in the users file to the array lists
     */
    private void read() {
        //READ IN OLD USERS
        if (users.length() != 0) {//if a user is already stored in the users file, read in the old users into the array
            try {
                Scanner input = new Scanner(users).useDelimiter("\\s*;\\s*");

                while (input.hasNext()) {//while there are still more users in the file that have not been read
                    String inName = input.next();
                    firstNames.add(inName);//add the next first name to the firstNames array list

                    String inLastName = input.next();
                    lastNames.add(inLastName);//add the next last name to the lastNames array list

                    String inUsername = input.next();
                    usernames.add(inUsername);//add the next username to the usernames array list

                    String inEmail = input.next();
                    emailAddresses.add(inEmail);//add the next email address to the email addresses array list

                    String inPassword = input.next();
                    passwords.add(inPassword);//add the next password to the passwords array list
                }

            } catch (FileNotFoundException ex) {
                System.out.printf("ERROR %s\n", ex);
            }
        }
    }
   

