    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //CHANGE USER'S PASSWORD

        clearFields();//clear the other status labels 
        String changeUser = jTextField8.getText();//get the first name of the user who wants to change their password
        String changePassword = jTextField9.getText();//get the password that the user wants to change their password to 
        String changedEncryptedPass = "";//string to store the encrypted version of the new password

        //ENCRYPT THE PASSWORD
        try {
            MessageDigest mesd = MessageDigest.getInstance("SHA-256");
            mesd.update(changePassword.getBytes());
            byte byteData3[] = mesd.digest();

            for (int i = 0; i < byteData3.length; ++i) {
                changedEncryptedPass += (Integer.toHexString((byteData3[i] & 0xFF) | 0x100).substring(1, 3));
            }

        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(SathishGauthamSecureLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

        int pos = 0;//variable to store position of user who wants to change passoword in the array lists

        if (firstNames.contains(changeUser)) {//if the user is a registered user

            pos = firstNames.indexOf(changeUser);//find the position of the user in the first names array list
            passwords.set(pos, changedEncryptedPass);//set the password in the same position in the passwords array list to the new encrypted password
            jLabel15.setText("Password Successfully Reset");//display successful password change message to user
            write();

        } else {

            jLabel15.setText("Not a Registered User.");//if the user is not a registered user, 
            jLabel16.setText("(Please make sure that you are entering your first name if you are already registered)");//reminder to user to enter first name
        }

        jTextField8.setText("");
        jTextField9.setText("");

    }//GEN-LAST:event_jButton3ActionPerformed

