    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //LOGIN BUTTON (for returning user)
        oldUsername = jTextField1.getText();//get username
        oldPassword = jTextField2.getText();//get password

        clearFields();//clear the other status labels 

        String oldEncryptedPass = "";//string to store encrypted version of password

        //ENCRYPT THE ENTERED PASSWORD
        try {
            MessageDigest mesd = MessageDigest.getInstance("SHA-256");
            mesd.update(oldPassword.getBytes());
            byte byteData2[] = mesd.digest();

            for (int i = 0; i < byteData2.length; ++i) {
                oldEncryptedPass += (Integer.toHexString((byteData2[i] & 0xFF) | 0x100).substring(1, 3));
            }

        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(SathishGauthamSecureLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

        //Check to see if username and encrypted password match a username and encrypted password which were stored in the user file
        if ((usernames.contains(oldUsername)) && (passwords.contains(oldEncryptedPass))) {//if both the username and the correct password are in the user file
            jLabel10.setText("Valid Login");

        } else {//if it does not match anything in the file
            jLabel10.setText("Not Registered");//tell the user that they are not registered
        }

        jTextField1.setText("");//clear the text field
        jTextField2.setText("");//clear the text field
    }//GEN-LAST:event_jButton1ActionPerformed

